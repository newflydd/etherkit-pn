#define FLASH_START_FW		( FLASH_START_FLL + FLASH_FLL_SIZE)
	/*------------------------------------------------------------------------------
	 *
	 * Function   : TcpFlashFirmware  (void* pBuf, PNIO_UINT32 BufSize)
	 *
	 * Description: erases the flash and programs new image
	 *
	 *
	 * Returns    : PNIO_OK, PNIO_NOT_OK
	 *
	 *----------------------------------------------------------------------------*/

PNIO_UINT32 TcpFlashFirmware (PNIO_UINT8 *pBuf, int FileSize)
{

	PNIO_UINT32 Status = PNIO_OK;
	unsigned int Error = 0;

	Status = FLASH_VERIFY((unsigned char*)FLASH_START_FLL, (unsigned char*)&g_abFLLoader[0], FLASH_FLL_SIZE);
	if(PNIO_OK != Status)
	{
		Status = FLASH_ERASE(FLASH_START_FLL, FLASH_FLL_SIZE, &Error);
		if(PNIO_OK == Status)
		{
			Status = FLASH_PROGRAM(FLASH_START_FLL,  (unsigned char*)&g_abFLLoader[0U], FLASH_FLL_SIZE, &Error);
		}
	}

	if(PNIO_OK == Status)
	{
		Status = FLASH_ERASE(FLASH_START_FW, FileSize, &Error);
	}

	if(PNIO_OK == Status)
	{
		Status = FLASH_PROGRAM(FLASH_START_FW, pBuf, FileSize, &Error);
	}

	if(PNIO_OK == Status)
	{
		Status = FLASH_VERIFY((unsigned char*)FLASH_START_FW, (unsigned char*)pBuf, FileSize);
	}

	 return Status;
}

